// Convert camelCase to kebab-case for URLs
export const getRouteFromId = (id) => {
  return id.replace(/([A-Z])/g, '-$1').toLowerCase();
};

// Convert kebab-case back to camelCase
export const getIdFromRoute = (route) => {
  return route.replace(/-([a-z])/g, (g) => g[1].toUpperCase());
};